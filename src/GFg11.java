
//Program to understand Wrapper class
import java .util.*;
class Gfg11 {

    public static void main(String args[]) {
        int a = 45;
        float b = 3.14f;
        long c=2340;

        // autoboxing
        Integer obj1 = a;
        Float obj2=b;
        Long obj3=c;
        // print
        System.out.println(obj1);
        System.out.println(obj2);
        System.out.println(obj3);
    }
}